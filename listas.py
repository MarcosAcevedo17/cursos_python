print("""
Una lista es una estructura de datos mutable y ordenada que permite almacenar una colección de elementos.
Los elementos pueden ser de diferentes tipos y se encierran entre corchetes [] separados por comas.
""")

# Creación de una lista
frutas = ["manzana", "banana", "naranja"]

print("Lista creada:", frutas)

print("\nAcceso a elementos por índice positivo:")
print(f"frutas[0]: {frutas[0]}")  # manzana
print(f"frutas[1]: {frutas[1]}")  # banana
print(f"frutas[2]: {frutas[2]}")  # naranja

print("\nAcceso a elementos por índice negativo:")
print(f"frutas[-1]: {frutas[-1]}")  # naranja
print(f"frutas[-2]: {frutas[-2]}")  # banana
print(f"frutas[-3]: {frutas[-3]}")  # manzana

