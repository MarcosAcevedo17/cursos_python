print("INDENTACIÓN")
print("En Python, la indentación (espacios o tabulaciones al inicio de una línea) se utiliza para delimitar bloques de código.")
print("A diferencia de otros lenguajes que utilizan llaves o palabras clave, Python utiliza la indentación para determinar el alcance de las declaraciones. Por ejemplo:")

print("""
if condition:
    # Bloque de código si la condición es verdadera
    instruccion1
    instruccion2
else:
    # Bloque de código si la condición es falsa
    instruccion3
    instruccion4
""")

print("====================================")

print("COMENTARIOS")
print("Los comentarios son líneas de texto en el código que son ignoradas por el intérprete de Python. Se utilizan para explicar o documentar el código.")
print("En Python, los comentarios de una sola línea comienzan con el símbolo #, mientras que los comentarios de varias líneas se encierran entre triples comillas. Por ejemplo:")

print("# Este es un comentario de una sola línea")

print('''"""
Este es un comentario
de varias líneas
"""''')

